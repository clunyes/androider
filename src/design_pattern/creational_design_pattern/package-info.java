package design_pattern.creational_design_pattern;
//创建型设计模式

/**
 * 几种工厂的区别：
 * 简单工厂（Simple Factory）模式：又称静态工厂方法模式（Static Factory Method Pattern）
 * 工厂方法（Factory Method）模式：又称多态性工厂（Polymorphic）模式或虚拟构造子（Virtual Constructor）模式。
 * 抽象工厂（Abstract Factory）模式，又称工具箱（Toolkit）模式。
 *
 *
 * 工厂模式区别
 *
 *     简单工厂 ： 使用一个工厂对象用来生产同一等级结构中的任意产品。（不支持拓展增加产品）
 *     工厂方法 ： 使用多个工厂对象用来生产同一等级结构中对应的固定产品。（支持拓展增加产品）
 *     抽象工厂 ： 使用多个工厂对象用来生产不同产品族的全部产品。（不支持拓展增加产品；支持增加产品族）
 */