package design_pattern.structural_design_pattern.bridge;
//桥接（bridge）是用于把抽象化与实现化解耦，使得二者可以独立变化。
// 多用于单个对象在多个维度有多个变化的值，比如笔可以画画，颜色，大小，粗细，这些都能变化，把这些变化的实现和声明分离


/**
 *适配模式与桥接模式的区别
 *
 * 所以说，如果你拿到两个已有模块，想让他们同时工作，那么你使用的适配器。
 * 如果你还什么都没有，但是想分开实现，那么桥接是一个选择。
 *
 * 桥接是先有桥，才有两端的东西
 * 适配是先有两边的东西，才有适配器
 *
 * 桥接是在桥好了之后，两边的东西还可以变化。
 */



